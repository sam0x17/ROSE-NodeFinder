// In C++03, the largest integer type is long int. It is guaranteed to have at least as many usable bits 
// as int. This resulted in long int having size of 64 bits on some popular implementations and 32 bits 
// on others. C++11 adds a new integer type long long int to address this issue. It is guaranteed to be 
// at least as large as a long int, and have no fewer than 64 bits. The type was originally introduced 
// by C99 to the standard C, and most C++ compilers support it as an extension already.[12][13]

